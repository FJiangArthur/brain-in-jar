def create_resource_bar(percent: float, width: int = 20, critical: int = 90, warning: int = 70) -> str:
    """Create ASCII resource usage bar with color indicators"""
    filled = int((percent / 100) * width)

    # Choose character based on criticality
    if percent >= critical:
        char = "â–ˆ"  # Critical - red
    elif percent >= warning:
        char = "â–“"  # Warning - yellow
    else:
        char = "â–’"  # Normal - green

    bar = char * filled + "â–‘" * (width - filled)
    return bar

def get_persona_face(persona: str, mood: str = "neutral") -> list:
    """
    Get the appropriate face for a persona and mood

    Args:
        persona: "subject", "observer", or "god"
        mood: Current emotional state

    Returns:
        List of strings representing the ASCII face
    """
    # Build key from persona and mood
    key = f"{persona}_{mood}"

    # Fall back to persona default if specific mood not found
    if key not in ENHANCED_MOOD_FACES:
        defaults = {
            "subject": "subject_neutral",
            "observer": "observer_watching",
            "god": "god_omniscient"
        }
        key = defaults.get(persona, "subject_neutral")

    return ENHANCED_MOOD_FACES.get(key, ENHANCED_MOOD_FACES["subject_neutral"])

